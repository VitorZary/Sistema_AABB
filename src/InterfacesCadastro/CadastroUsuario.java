    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String senhaTxt = new String(txtPasswSenha.getPassword());
        String CsenhaTxt = new String(txtPasswConfirmarSenha.getPassword());
        
        if(((senhaTxt.equals(""))||(txtLogin.getText().equals(""))||
        (txtNome.getText().equals(""))||(CsenhaTxt.equals("")))){
            JOptionPane.showMessageDialog(this, "Verificar campo em branco!");  
            
        }else{
            if(senhaTxt.equals(CsenhaTxt)==false){
                JOptionPane.showMessageDialog(this, "As senhas n√£o correspondem!");
        
            }else{
                btnCancelar.setEnabled(false);
                btnNovo.setEnabled(true);
                btnEditar.setEnabled(false);
                btnSalvar.setEnabled(false);
                txtNome.setEnabled(false);
                txtPasswConfirmarSenha.setEnabled(false);
                txtLogin.setEnabled(false);
                txtPasswSenha.setEnabled(false);
                checkboxMostrarS.setEnabled(false);
                checkboxMostrarSenhaConfirmarS.setEnabled(false);


                String nome = txtNome.getText();
                String login = txtLogin.getText();
                String senhA = new String(txtPasswSenha.getPassword());
                
                if(editar ==true){
                    
                    int pos = tableUsuarios.getSelectedRow();
                    Usuario.usuario.get(pos).setNome(nome);
                    Usuario.usuario.get(pos).setSenha(senhA);
                    Usuario.usuario.get(pos).setLogin(login);
                    tableUsuarios.setValueAt(nome, pos, 0);
                    tableUsuarios.setValueAt(login, pos, 1);
                    checkboxMostrarS.setEnabled(false);
                    checkboxMostrarSenhaConfirmarS.setEnabled(false);
                    JOptionPane.showMessageDialog(null,"Dados alterados com sucesso!");
                    editar = false;

                }else{
                    Usuario usuario = new Usuario();

                    usuario.cadastrar(nome, senhA, login);
                    Usuario.usuario.add(usuario);
                    DefaultTableModel dtm = (DefaultTableModel) tableUsuarios.getModel();

                    dtm.addRow(new Object[]{nome, login});

                    JOptionPane.showMessageDialog(null,"Dados cadastrados com sucesso!");
                    
                }
                
                txtNome.setText("");
                txtPasswConfirmarSenha.setText("");
                txtPasswSenha.setText("");
                txtLogin.setText("");
                tableUsuarios.setEnabled(true);
                tableUsuarios.setVisible(true);
            }

        }
    }//GEN-LAST:event_btnSalvarActionPerformed

