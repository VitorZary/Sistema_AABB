    private void btnRegistrarPagamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarPagamentoActionPerformed
        float valor = 0;
        Object obj = tableControlePagamento.getModel().getValueAt(tableControlePagamento.getSelectedRow() ,0);
        String str = String.valueOf(obj);
        int id = Integer.parseInt(str);
        int posicao = 0;
        
        for(int i = 0; i < Titular.titular.size(); i++){
                if(Titular.titular.get(i).getCodAssociado()==id){
                        posicao = i;
                    }
                }

        for(int i=0; i<Titular.titular.get(posicao).codDependente.size();i++){
            valor = valor + Configuracao.getTaxaDependente();
        }
        
        valor = valor + Configuracao.getMensalidadeTitular();
        
        if(JOptionPane.showConfirmDialog(null, "Deseja realmente efetuar o registro?\nValor a se pagar: R$ "+valor, "Pergunta", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE)== JOptionPane.YES_OPTION){
            Titular.titular.get(posicao).setQuitacao(true);
        }
        
        DefaultTableModel dtm = (DefaultTableModel) tableControlePagamento.getModel();
        
        ((DefaultTableModel) tableControlePagamento.getModel()).setRowCount(0);
        
        for (int i = 0; i < Titular.titular.size(); i++) {
            if(Titular.titular.get(i).isQuitacao()==false)
                dtm.addRow(new Object[]{Titular.titular.get(i).getCodAssociado(),Titular.titular.get(i).getNome(),Titular.titular.get(i).getCpf()
            });
        
        }
    }//GEN-LAST:event_btnRegistrarPagamentoActionPerformed

