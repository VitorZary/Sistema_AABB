    private void txtBuscaNomeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtBuscaNomeActionPerformed
        DefaultTableModel dtm = (DefaultTableModel) tableControlePagamento.getModel();
        
        ((DefaultTableModel) tableControlePagamento.getModel()).setRowCount(0);
        
        String txt = txtBuscaNome.getText();
        
        for (int i = 0; i < Titular.titular.size(); i++) {
            
            if((Titular.titular.get(i).isQuitacao()==false)&&
               (Titular.titular.get(i).getNome().contains(txt))==true&&
                (Titular.titular.get(i).isSituacao()==true))
                dtm.addRow(new Object[]{Titular.titular.get(i).getCodAssociado(),Titular.titular.get(i).getNome(),
                                        Titular.titular.get(i).getCpf()});

        }
        
    }//GEN-LAST:event_txtBuscaNomeActionPerformed

